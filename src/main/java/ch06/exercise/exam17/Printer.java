package ch06.exercise.exam17;

public class Printer {
    static int println(int a) {
        System.out.println(a);
        return a;
    }

    static boolean println(boolean a) {
        System.out.println(a);
        return a;
    }


    static double println(double a) {
        System.out.println(a);
        return a;
    }

    static String println(String a) {
        System.out.println(a);
        return a;
    }
}
